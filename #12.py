# Mảng ban đầu
my_array = [1, 2, 3, 1, 2, 3, 1]

# Giá trị cần kiểm tra
value_to_count = 1

# Sử dụng phương thức count() để kiểm tra số lần xuất hiện của giá trị trong mảng
count_of_value = my_array.count(value_to_count)

# In số lần xuất hiện của giá trị
print(f"Số lần xuất hiện của giá trị {value_to_count} trong mảng là {count_of_value}")