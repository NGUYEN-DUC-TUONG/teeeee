# Tạo một mảng
my_array = [1, 20, 3, 21, 5]
# Truy cập các phần tử riêng lẻ thông qua chỉ mục
# Chú ý rằng chỉ mục bắt đầu từ 0
first_element = my_array[0]
second_element = my_array[1]
third_element = my_array[2]
# In giá trị của các phần tử đã truy cập
print("Phần tử đầu tiên:", first_element)
print("Phần tử thứ hai:", second_element)
print("Phần tử thứ ba:", third_element)