# Mảng ban đầu
my_array = [1, 2, 3, 4, 5]
# Sử dụng pop() để loại bỏ và trả về phần tử cuối cùng của mảng
removed_element = my_array.pop()
# In mảng sau khi loại bỏ phần tử cuối cùng
print("Mảng sau khi loại bỏ phần tử cuối cùng:", my_array)
# In phần tử cuối cùng đã bị loại bỏ
print("Phần tử cuối cùng đã bị loại bỏ:", removed_element)